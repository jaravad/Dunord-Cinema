    private void accederMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_accederMouseClicked
        if ((usertxt.getText().length() != 0) && (passwordfield.getText().length() != 0)) {
            String usuario = usertxt.getText();
            String password = passwordfield.getText();
            File archivo = new File("administradores.txt");
            Scanner s = null;
            try {
                boolean sw = false, sw2 = false;
                s = new Scanner(archivo);
                while (s.hasNextLine()) {
                    String linea = s.nextLine();
                    String[] campo = linea.split(",");
                    if (usuario.equals(campo[2]) && password.equals(campo[3])) {
                        sw = true;
                        sw2 = true;
                        break;
                    } else if (usuario.equals(campo[2]) && !password.equals(campo[3])) {
                        sw = true;
                        sw2 = false;
                        break;
                    } else {
                        sw = false;
                        sw2 = false;
                    }

                }
                if ((sw == false) && (sw2 == false)) {
                    JOptionPane.showMessageDialog(null, "El Administrador no existe", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                } else if ((sw == true) && (sw2 == false)) {
                    JOptionPane.showMessageDialog(null, "Contraseña incorrecta", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                } else if (sw == true && sw2 == true) {
                    this.setVisible(false);
                    Admin ventana=new Admin();
                    ventana.setVisible(true);
                }
            } catch (Exception ex) {
                System.out.println("Mensaje:" + ex.getMessage());
            } finally {
                try {
                    if (s != null) {
                        s.close();
                    }
                } catch (Exception ex2) {
                    System.out.println("Mensaje 2:" + ex2.getMessage());
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "No deben haber campos vacios", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_accederMouseClicked

